# 파이썬 변수: 수치형
print("*"  * 100)
# 숫자형, 모든것이 객체, 1도 객체, a는 객체의 주소를 참조하고 있다.
a = 1
b = -1
c = 0
print("정수형 > ", a, b, c)
print("*"  * 100)
# 실수형
a = 1.2
b = -2.
print("실수형 > ", a, b)
print("*"  * 100)
# 8진수, 16진수, 복소수 가능
a = 0xFF
print("진수 > ", a)
print("*"  * 100)
# 사칙연산
a = 1 
b = 5
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a * * b =", a ** b)
print("a % b =", a % b)
print("*"  * 100)
